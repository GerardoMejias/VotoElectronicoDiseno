package cr.ac.itcr.votoelectronico1;

public class VotoRegistrado {

    private String uid;
    private String uidPersona;
    private String uidPropuesta;

    public VotoRegistrado(){}

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUidPersona() {
        return uidPersona;
    }

    public void setUidPersona(String uidPersona) {
        this.uidPersona = uidPersona;
    }

    public String getUidPropuesta() {
        return uidPropuesta;
    }

    public void setUidPropuesta(String uidPropuesta) {
        this.uidPropuesta = uidPropuesta;
    }
}
